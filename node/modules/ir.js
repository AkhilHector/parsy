module.exports.cosine_sim = (sentence_one, sentence_two) => {
    return sentence_one + sentence_two
}

module.exports.ngrams = (sentence_one, sentence_two) => {
    return sentence_one, sentence_two
}

module.exports.tfidf = (sentence_one, sentence_two) => {
    return sentence_one, sentence_two
}
